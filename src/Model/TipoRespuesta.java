package Model;

public class TipoRespuesta {

	private int correcto, incorrecto, moldeamiento, ayuda;

	public int getCorrecto() {
		return correcto;
	}

	public void setCorrecto(int correcto) {
		this.correcto = correcto;
	}

	public int getIncorrecto() {
		return incorrecto;
	}

	public void setIncorrecto(int incorrecto) {
		this.incorrecto = incorrecto;
	}

	public int getMoldeamiento() {
		return moldeamiento;
	}

	public void setMoldeamiento(int moldeamiento) {
		this.moldeamiento = moldeamiento;
	}

	public int getAyuda() {
		return ayuda;
	}

	public void setAyuda(int ayuda) {
		this.ayuda = ayuda;
	}

}
